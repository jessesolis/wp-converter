export const THEME_SLUG = "scorpion-converted";

export interface PerPageAssets {
  // Template slug (e.g. "home" or "residential-plumbing-services") →
  // ordered list of CSS / JS filenames the page should load. Filenames
  // resolve to files in theme/css/ and theme/js/ respectively, plus the
  // per-page inline CSS filename (also written to theme/css/).
  cssFilenamesByTemplateSlug: Map<string, string[]>;
  jsFilenamesByTemplateSlug: Map<string, string[]>;
}

export interface ThemeInputs {
  siteTitle: string;
  // Every CSS / JS filename present in theme/css/ and theme/js/. Each
  // becomes a wp_register_style/script call so the per-page enqueue can
  // reference it by handle.
  cssFilenames: string[];
  jsFilenames: string[];
  perPage: PerPageAssets;
  // Template slug used for `post_type=post` views via single.php. Picked
  // from the dominant Scorpion template among blog posts. If null the
  // post enqueue branch is a no-op (no blog posts on this site).
  postTemplateSlug: string | null;
}

export function buildStyleCss(siteTitle: string): string {
  const safeTitle = escapeCssComment(siteTitle);
  return `/*
Theme Name: Scorpion Converted — ${safeTitle}
Description: Auto-generated by the Scorpion CMS → WordPress conversion tool.
Version: 1.0
*/
`;
}

export function buildFunctionsPhp(inputs: ThemeInputs): string {
  const cssHandleByFilename = new Map<string, string>();
  inputs.cssFilenames.forEach((filename, i) => {
    cssHandleByFilename.set(filename, `scorpion-style-${i + 1}`);
  });
  const jsHandleByFilename = new Map<string, string>();
  inputs.jsFilenames.forEach((filename, i) => {
    jsHandleByFilename.set(filename, `scorpion-script-${i + 1}`);
  });

  const registerStyles = inputs.cssFilenames
    .map((filename) => {
      const handle = cssHandleByFilename.get(filename)!;
      return `    wp_register_style('${handle}', get_stylesheet_directory_uri() . '/css/${escapePhp(filename)}', [], null);`;
    })
    .join("\n");

  const registerScripts = inputs.jsFilenames
    .map((filename) => {
      const handle = jsHandleByFilename.get(filename)!;
      return `    wp_register_script('${handle}', get_stylesheet_directory_uri() . '/js/${escapePhp(filename)}', [], null, true);`;
    })
    .join("\n");

  const pageAssetsPhp = buildPageAssetsPhpArray(
    inputs.perPage,
    cssHandleByFilename,
    jsHandleByFilename,
  );

  const postSlugPhp = inputs.postTemplateSlug
    ? `'${escapePhp(inputs.postTemplateSlug)}'`
    : "null";

  return `<?php
/**
 * Scorpion Converted child theme — generated.
 *
 * Stylesheets and scripts are registered globally then enqueued per page,
 * keyed by the page's template slug. The browser only loads the assets the
 * original Scorpion page actually used.
 */
function scorpion_converted_register_assets() {
${registerStyles || "    // (no stylesheets discovered)"}
${registerScripts || "    // (no scripts discovered)"}
}
add_action('wp_enqueue_scripts', 'scorpion_converted_register_assets', 5);

// Map of template slug → ['css' => [handles], 'js' => [handles]] in the
// original document load order from each Scorpion page.
function scorpion_converted_page_assets_map() {
    return ${pageAssetsPhp};
}

function scorpion_converted_current_template_slug() {
    if (is_singular('post')) {
        // Blog posts use the chrome of whichever Scorpion template was
        // most common among the site's blog posts at conversion time.
        // null when the site has no blog posts.
        return ${postSlugPhp};
    }
    if (!is_page()) {
        return null;
    }
    $tpl = get_page_template_slug();
    if (!is_string($tpl) || $tpl === '') {
        return null;
    }
    if (preg_match('#templates/page-(.+)\\.php$#', $tpl, $m)) {
        return $m[1];
    }
    return null;
}

function scorpion_converted_enqueue_page_assets() {
    $slug = scorpion_converted_current_template_slug();
    if ($slug === null) {
        return;
    }
    $map = scorpion_converted_page_assets_map();
    if (!isset($map[$slug])) {
        return;
    }
    $entry = $map[$slug];
    if (!empty($entry['css'])) {
        foreach ($entry['css'] as $handle) {
            wp_enqueue_style($handle);
        }
    }
    if (!empty($entry['js'])) {
        foreach ($entry['js'] as $handle) {
            wp_enqueue_script($handle);
        }
    }
}
add_action('wp_enqueue_scripts', 'scorpion_converted_enqueue_page_assets', 10);

/**
 * Intercept /common/usc/p/<name>.{js,html,css} requests and serve the
 * corresponding file out of the theme's js/ dir.
 *
 * Scorpion's require2() module loader (inlined into every page's HTML)
 * builds runtime URLs of the form /common/usc/p/<name>.js from bare
 * module names like require2('usc/p/scrolling-list'). The literal
 * /common/usc/p/ prefix is never present in the source HTML or JS — it's
 * concatenated at runtime — so an HTML / JS source rewrite can't catch
 * it. A request-time intercept covers every reference form (static,
 * runtime-constructed, ES module import) transparently.
 *

 * Apache / Nginx route the URL to WP because the file doesn't exist on
 * disk; the init hook fires before WP's 404 handler so we can short-
 * circuit and stream the theme file directly.
 */
function scorpion_converted_serve_usc_p_request() {
    if (!isset($_SERVER['REQUEST_URI'])) {
        return;
    }
    $uri = (string) $_SERVER['REQUEST_URI'];
    $path = strtok($uri, '?');
    if (!preg_match('#^/common/usc/p/([a-zA-Z0-9._-]+\\.(js|html|css))$#', $path, $m)) {
        return;
    }
    $name = $m[1];
    $file = get_stylesheet_directory() . '/js/' . $name;
    if (!file_exists($file)) {
        return;
    }
    $ext = strtolower($m[2]);
    if ($ext === 'js') {
        $mime = 'application/javascript';
    } elseif ($ext === 'css') {
        $mime = 'text/css';
    } elseif ($ext === 'html') {
        $mime = 'text/html; charset=utf-8';
    } else {
        $mime = 'application/octet-stream';
    }
    header('Content-Type: ' . $mime);
    header('Cache-Control: public, max-age=31536000');
    readfile($file);
    exit;
}
add_action('init', 'scorpion_converted_serve_usc_p_request', 0);

/**
 * [scorpion_zone id="…"] — renders one content zone at its original DOM
 * position in the page template. Each zone's HTML is stored in postmeta
 * '_scorpion_zone_<id>' by the WXR importer; the template emits one
 * shortcode call per placeholder so multi-zone pages keep exact placement.
 */
function scorpion_zone_shortcode($atts) {
    $atts = shortcode_atts(array('id' => ''), $atts, 'scorpion_zone');
    $id = trim((string) $atts['id']);
    if ($id === '') {
        return '';
    }
    $value = get_post_meta(get_the_ID(), '_scorpion_zone_' . $id, true);
    return is_string($value) ? $value : '';
}
add_shortcode('scorpion_zone', 'scorpion_zone_shortcode');

/**
 * "Scorpion Zones" admin metabox — surfaces every '_scorpion_zone_<id>'
 * postmeta key on the page as a labeled textarea so admins can edit zone
 * HTML directly from the page editor. The original Scorpion markup lives
 * in '_scorpion_zone_<id>__original' (written once on import, never
 * overwritten); each zone has a per-zone Revert checkbox that copies the
 * original back over the editable value on save.
 */
function scorpion_converted_register_zones_metabox() {
    add_meta_box(
        'scorpion-zones',
        'Scorpion Zones',
        'scorpion_converted_render_zones_metabox',
        'page',
        'normal',
        'high'
    );
}
add_action('add_meta_boxes', 'scorpion_converted_register_zones_metabox');

function scorpion_converted_render_zones_metabox($post) {
    $meta = get_post_meta($post->ID);
    $zone_keys = array();
    foreach (array_keys($meta) as $key) {
        if (strpos($key, '_scorpion_zone_') !== 0) {
            continue;
        }
        if (substr($key, -strlen('__original')) === '__original') {
            continue;
        }
        $zone_keys[] = $key;
    }
    sort($zone_keys);

    wp_nonce_field('scorpion_zones_save', 'scorpion_zones_nonce');

    if (empty($zone_keys)) {
        echo '<p><em>No Scorpion zones recorded for this page.</em></p>';
        return;
    }

    echo '<p style="margin:0 0 1em;color:#555;">Each block below is a content region from the original Scorpion page, rendered at its DOM position via <code>[scorpion_zone id="…"]</code>. Edit visually — switch to the <strong>Text</strong> tab if you need to touch raw HTML. To restore the original markup, check <strong>Revert to original</strong> and click <strong>Update</strong>.</p>';

    foreach ($zone_keys as $key) {
        $zone_id = substr($key, strlen('_scorpion_zone_'));
        $current = get_post_meta($post->ID, $key, true);
        if (!is_string($current)) {
            $current = '';
        }
        $has_original = metadata_exists('post', $post->ID, $key . '__original');
        $field_name = 'scorpion_zone_' . $zone_id;
        $revert_name = 'scorpion_zone_revert_' . $zone_id;
        $editor_id = 'scorpion_zone_editor_' . preg_replace('/[^a-z0-9]/i', '_', $zone_id);

        echo '<details style="margin:0 0 0.75em;border:1px solid #ddd;border-radius:4px;" open>';
        echo '<summary style="padding:0.5em 0.75em;background:#f6f7f7;cursor:pointer;font-family:monospace;font-size:13px;">' . esc_html($zone_id) . '</summary>';
        echo '<div style="padding:0.75em;">';
        // wp_editor() renders TinyMCE (visual + text tab). The textarea_name
        // tells WP what $_POST key our save handler should read on submit;
        // the editor_id has to be unique per metabox instance.
        // tinymce.wpautop is left at WP's defaults — the Visual tab will
        // pretty-print the HTML, but switching to the Text tab and saving
        // preserves whatever raw HTML you type there verbatim.
        wp_editor(
            $current,
            $editor_id,
            array(
                'textarea_name' => $field_name,
                'media_buttons' => true,
                'tinymce'       => array('wpautop' => false),
                'quicktags'     => true,
                'editor_height' => 240,
            )
        );
        if ($has_original) {
            echo '<label style="display:block;margin-top:0.5em;font-size:13px;">';
            echo '<input type="checkbox" name="' . esc_attr($revert_name) . '" value="1" /> ';
            echo 'Revert to original on save (discards edits to this zone)';
            echo '</label>';
        } else {
            echo '<p style="margin-top:0.5em;font-size:12px;color:#888;"><em>No original snapshot recorded — Revert unavailable.</em></p>';
        }
        echo '</div></details>';
    }
}

function scorpion_converted_save_zones($post_id) {
    if (!isset($_POST['scorpion_zones_nonce'])) {
        return;
    }
    if (!wp_verify_nonce(wp_unslash($_POST['scorpion_zones_nonce']), 'scorpion_zones_save')) {
        return;
    }
    if (defined('DOING_AUTOSAVE') && DOING_AUTOSAVE) {
        return;
    }
    if (!current_user_can('edit_post', $post_id)) {
        return;
    }

    $meta = get_post_meta($post_id);
    foreach (array_keys($meta) as $key) {
        if (strpos($key, '_scorpion_zone_') !== 0) {
            continue;
        }
        if (substr($key, -strlen('__original')) === '__original') {
            continue;
        }

        $zone_id = substr($key, strlen('_scorpion_zone_'));
        $revert_name = 'scorpion_zone_revert_' . $zone_id;
        $field_name = 'scorpion_zone_' . $zone_id;

        if (!empty($_POST[$revert_name])) {
            $original = get_post_meta($post_id, $key . '__original', true);
            if (is_string($original)) {
                update_post_meta($post_id, $key, $original);
            }
            continue;
        }

        if (array_key_exists($field_name, $_POST)) {
            // Admins editing the converted theme — store the raw HTML exactly
            // as typed. wp_unslash() reverses WP's magic-quotes; we
            // deliberately do NOT call wp_kses_post() because the source
            // HTML uses arbitrary classes / data-* attrs that strict
            // sanitisation would strip.
            $new_value = wp_unslash($_POST[$field_name]);
            update_post_meta($post_id, $key, $new_value);
        }
    }
}
add_action('save_post_page', 'scorpion_converted_save_zones');
`;
}

export function buildIndexPhp(): string {
  return `<?php
/**
 * Minimal fallback template. Per-page templates live in templates/.
 */
get_header();
if (have_posts()) {
    while (have_posts()) {
        the_post();
        the_content();
    }
}
get_footer();
`;
}

function buildPageAssetsPhpArray(
  perPage: PerPageAssets,
  cssHandleByFilename: Map<string, string>,
  jsHandleByFilename: Map<string, string>,
): string {
  const slugs = new Set<string>([
    ...perPage.cssFilenamesByTemplateSlug.keys(),
    ...perPage.jsFilenamesByTemplateSlug.keys(),
  ]);
  if (slugs.size === 0) {
    return "array()";
  }

  const sortedSlugs = [...slugs].sort();
  const lines: string[] = ["array("];
  for (const slug of sortedSlugs) {
    const cssFiles = perPage.cssFilenamesByTemplateSlug.get(slug) ?? [];
    const jsFiles = perPage.jsFilenamesByTemplateSlug.get(slug) ?? [];
    const cssHandles = cssFiles
      .map((f) => cssHandleByFilename.get(f))
      .filter((h): h is string => !!h);
    const jsHandles = jsFiles
      .map((f) => jsHandleByFilename.get(f))
      .filter((h): h is string => !!h);

    lines.push(`        '${escapePhp(slug)}' => array(`);
    lines.push(`            'css' => ${phpStringArray(cssHandles)},`);
    lines.push(`            'js'  => ${phpStringArray(jsHandles)},`);
    lines.push(`        ),`);
  }
  lines.push("    )");
  return lines.join("\n");
}

function phpStringArray(values: string[]): string {
  if (values.length === 0) return "array()";
  const inner = values.map((v) => `'${escapePhp(v)}'`).join(", ");
  return `array(${inner})`;
}

function escapeCssComment(value: string): string {
  return value.replace(/\*\//g, "*\\/");
}

function escapePhp(value: string): string {
  return value.replace(/'/g, "\\'");
}
