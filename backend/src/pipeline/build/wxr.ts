import type { ScorpionPage } from "../ingest";
import type { PageContentZones } from "../parse";
import { rewriteHtmlUrls } from "./url-rewriter";

export interface WxrInputs {
  siteUrl: string;
  siteTitle: string;
  pages: ScorpionPage[];
  contentZones: PageContentZones[];
  pathToSlug: Map<string, string>;
  urlMap: Map<string, string>;
}

export function buildWxrXml(inputs: WxrInputs): string {
  const zonesByPath = new Map(
    inputs.contentZones.map((cz) => [cz.path, cz]),
  );
  const pubDate = new Date().toUTCString();

  const items = inputs.pages
    .map((page, idx) => buildPageItem(page, idx, zonesByPath, inputs))
    .join("\n");

  return `<?xml version="1.0" encoding="UTF-8"?>
<rss version="2.0"
  xmlns:excerpt="http://wordpress.org/export/1.2/excerpt/"
  xmlns:content="http://purl.org/rss/1.0/modules/content/"
  xmlns:wfw="http://wellformedweb.org/CommentAPI/"
  xmlns:dc="http://purl.org/dc/elements/1.1/"
  xmlns:wp="http://wordpress.org/export/1.2/">
  <channel>
    <title>${xmlText(inputs.siteTitle)}</title>
    <link>${xmlText(inputs.siteUrl)}</link>
    <description>Auto-generated by the Scorpion CMS → WordPress conversion tool.</description>
    <pubDate>${pubDate}</pubDate>
    <language>en-US</language>
    <wp:wxr_version>1.2</wp:wxr_version>
    <wp:base_site_url>${xmlText(inputs.siteUrl)}</wp:base_site_url>
    <wp:base_blog_url>${xmlText(inputs.siteUrl)}</wp:base_blog_url>
    <wp:author>
      <wp:author_id>1</wp:author_id>
      <wp:author_login><![CDATA[admin]]></wp:author_login>
      <wp:author_email><![CDATA[admin@example.com]]></wp:author_email>
      <wp:author_display_name><![CDATA[admin]]></wp:author_display_name>
      <wp:author_first_name><![CDATA[]]></wp:author_first_name>
      <wp:author_last_name><![CDATA[]]></wp:author_last_name>
    </wp:author>
${items}
  </channel>
</rss>
`;
}

function buildPageItem(
  page: ScorpionPage,
  idx: number,
  zonesByPath: Map<string, PageContentZones>,
  inputs: WxrInputs,
): string {
  const slug = inputs.pathToSlug.get(page.path) ?? `page-${idx + 1}`;
  const zones = zonesByPath.get(page.path);
  const pageUrl = zones?.pageUrl ?? page.canonical;
  const blocks = (zones?.zones ?? [])
    .map((z) => {
      const innerHtml = rewriteHtmlUrls(z.innerHtml, pageUrl, inputs.urlMap);
      return `<!-- wp:freeform -->\n${innerHtml}\n<!-- /wp:freeform -->`;
    })
    .join("\n\n");
  const pubDate = new Date().toUTCString();

  const meta = [
    postmeta("_yoast_wpseo_title", page.metaTitle),
    postmeta("_yoast_wpseo_metadesc", page.metaDescription),
    postmeta("_yoast_wpseo_canonical", page.canonical),
  ]
    .filter((s) => s.length > 0)
    .join("\n");

  return `    <item>
      <title>${xmlText(page.title || slug)}</title>
      <link>${xmlText(page.canonical)}</link>
      <pubDate>${pubDate}</pubDate>
      <dc:creator><![CDATA[admin]]></dc:creator>
      <guid isPermaLink="false">${xmlText(page.canonical)}</guid>
      <description></description>
      <content:encoded>${cdata(blocks)}</content:encoded>
      <excerpt:encoded><![CDATA[]]></excerpt:encoded>
      <wp:post_id>${idx + 1}</wp:post_id>
      <wp:post_date><![CDATA[${formatMysqlDate(new Date())}]]></wp:post_date>
      <wp:post_date_gmt><![CDATA[${formatMysqlDate(new Date())}]]></wp:post_date_gmt>
      <wp:comment_status><![CDATA[closed]]></wp:comment_status>
      <wp:ping_status><![CDATA[closed]]></wp:ping_status>
      <wp:post_name><![CDATA[${slug}]]></wp:post_name>
      <wp:status><![CDATA[publish]]></wp:status>
      <wp:post_parent>0</wp:post_parent>
      <wp:menu_order>${idx}</wp:menu_order>
      <wp:post_type><![CDATA[page]]></wp:post_type>
      <wp:post_password><![CDATA[]]></wp:post_password>
      <wp:is_sticky>0</wp:is_sticky>
      <wp:page_template><![CDATA[templates/page-${slug}.php]]></wp:page_template>
${meta}
    </item>`;
}

function postmeta(key: string, value: string | undefined): string {
  if (!value) return "";
  return `      <wp:postmeta>
        <wp:meta_key><![CDATA[${key}]]></wp:meta_key>
        <wp:meta_value>${cdata(value)}</wp:meta_value>
      </wp:postmeta>`;
}

function xmlText(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function cdata(text: string): string {
  // Split any "]]>" so it doesn't terminate the CDATA section.
  return `<![CDATA[${text.replace(/]]>/g, "]]]]><![CDATA[>")}]]>`;
}

function formatMysqlDate(d: Date): string {
  const pad = (n: number) => String(n).padStart(2, "0");
  return `${d.getUTCFullYear()}-${pad(d.getUTCMonth() + 1)}-${pad(d.getUTCDate())} ${pad(d.getUTCHours())}:${pad(d.getUTCMinutes())}:${pad(d.getUTCSeconds())}`;
}
