export interface ChecklistInputs {
  siteTitle: string;
  pageCount: number;
  zoneCount: number;
  mediaCount: number;
  failedMedia: number;
  formVariantCount: number;
  redirectCount: number;
  knownLimitations: string[];
}

export function buildMigrationChecklist(inputs: ChecklistInputs): string {
  const limitations = inputs.knownLimitations.length > 0
    ? inputs.knownLimitations.map((l) => `- ${l}`).join("\n")
    : "- None recorded for this conversion.";

  return `# Migration Checklist — ${inputs.siteTitle}

This package was generated by the Scorpion CMS → WordPress conversion tool.

## What's inside
- \`theme/scorpion-converted/\` — child theme with all extracted CSS, JS, and per-page templates
- \`media/\` — every same-origin image/document discovered on the live site
- \`import.xml\` — WXR file containing ${inputs.pageCount} page${inputs.pageCount === 1 ? "" : "s"} and ${inputs.zoneCount} content-zone post-meta entr${inputs.zoneCount === 1 ? "y" : "ies"}, plus SEO post-meta (Yoast keys)

## Stats
- Pages: ${inputs.pageCount}
- Content-zone post-meta entries: ${inputs.zoneCount}
- Media assets: ${inputs.mediaCount}${inputs.failedMedia > 0 ? ` (${inputs.failedMedia} failed to download — see below)` : ""}
- Form variants: ${inputs.formVariantCount}
- 301 redirects: ${inputs.redirectCount}

## Post-import steps

1. Install and activate the \`scorpion-converted\` child theme (\`theme/scorpion-converted/\`).
2. Upload the contents of \`media/\` to \`wp-content/uploads/scorpion-migration/\` on the WP server.
3. In WP admin: \`Tools → Import → WordPress\` and upload \`import.xml\`. Assign all posts to an existing author when prompted.
4. Install Yoast SEO (or your preferred SEO plugin) — meta titles, descriptions, and canonicals are imported as Yoast post-meta keys.
5. Each imported page has \`templates/page-{slug}.php\` assigned via Page Template. Verify the Page Template dropdown is set correctly in the page editor.
6. Visually compare every page against the original Scorpion site. Differences worth investigating: missing images, broken layout, missing interactive components.
7. Install + activate **Contact Form 7**. The WXR contains one \`wpcf7_contact_form\` post per detected form variant (${inputs.formVariantCount} total), and Scorpion \`<form>\` markup in templates has been swapped for \`[contact-form-7 id="…"]\` shortcodes. After import, open **wp-admin → Contact** and for each form set the **Mail → To** address (defaults to a placeholder); the Form, Subject, and Body templates are pre-populated from the captured field structure.
8. ${inputs.redirectCount > 0 ? `Install + activate **Redirection** and import \\\`redirects.csv\\\` at **Tools → Redirection → Import/Export** (${inputs.redirectCount} rules total). \`npm run wp:import\` automates this locally.` : "(No 301 redirects in this conversion — Redirection plugin not needed.)"}
9. Test all interactive components (sliders, accordions, tabs) — Scorpion's JS utilities are bundled into the theme \`js/\` directory.

## Known limitations
${limitations}

## Notes
- All stylesheets and JS are enqueued globally via \`functions.php\`. Conditional per-page enqueueing is deferred as a future optimisation.
- Each content zone is stored as a postmeta key \`_scorpion_zone_<zoneId>\` and rendered at its original DOM position via the \`[scorpion_zone id="…"]\` shortcode registered in \`functions.php\`. To edit a zone, open the page in wp-admin — the **Scorpion Zones** metabox lists every zone for the page as a labeled textarea. The original Scorpion markup is preserved in \`_scorpion_zone_<zoneId>__original\` so each zone can be reverted via the metabox's per-zone Revert checkbox.
- Media URLs in templates and zone meta have been rewritten to \`/wp-content/uploads/scorpion-migration/{filename}\`. URLs the tool could not resolve to a downloaded file remain at their original absolute Scorpion-domain location.
- 301 redirects from Scorpion's \`#SiteRedirectTable\` are exported as \`redirects.csv\` and imported into the **Redirection** plugin (auto-installed by \`wp:import\` only when the CSV is present). After import, edit / add / delete redirects at **Tools → Redirection** in wp-admin — Redirection also adds regex, hit logging, and conflict detection on top.
`;
}
